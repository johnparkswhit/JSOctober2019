// Assignment Operator
let r = 5;

//Addition assignment
r += 1; //r = r + 1;

//Subtraction Assignment
r -= 1; //r = r - 1;

//Multiplication Assignment
r *= 1; //r = r * 1;

//Division Assignment
r /= 1; //r = r / 1;

//Remainder Assignment/Modulus Assignment
r %= 1; //r = r % 1;

//Exponential Assignment ** means "to the power of"
r **= 1; //r = r ** 1
